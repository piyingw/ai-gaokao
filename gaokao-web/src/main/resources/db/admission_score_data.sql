-- 4. 数据统计视图（可选）
-- =============================================

-- 创建院校分数线统计视图
CREATE OR REPLACE VIEW v_university_score_stats AS
SELECT 
    u.id AS university_id,
    u.name AS university_name,
    u.level,
    s.province,
    s.subject_type,
    COUNT(*) AS record_count,
    MIN(s.min_score) AS min_score_min,
    MAX(s.min_score) AS min_score_max,
    ROUND(AVG(s.min_score), 2) AS min_score_avg,
    MIN(s.min_rank) AS min_rank_min,
    MAX(s.min_rank) AS min_rank_max,
    ROUND(AVG(s.min_rank), 0) AS min_rank_avg
FROM university u
JOIN admission_score s ON u.id = s.university_id
GROUP BY u.id, u.name, u.level, s.province, s.subject_type;

