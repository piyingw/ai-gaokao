def main():
    """主函数"""
    print("加载已有数据...")
    universities = load_existing_data()
    
    # 获取已有院校名称
    existing_names = {uni['name'] for uni in universities}
    
    # 获取最大ID
    max_id = max(uni['id'] for uni in universities)
    
    # 添加新院校
    new_count = 0
    
    # 添加批次院校
    for province, unis in BATCH_UNIVERSITIES.items():
        for name, code, utype, nature in unis:
            if name not in existing_names:
                max_id += 1
                universities.append({
                    'id': max_id,
                    'name': name,
                    'code': code,
                    'province': province,
                    'city': get_city(province, name),
                    'level': '普通本科',
                    'type': utype,
                    'nature': nature,
                    'ranking': max_id + 600
                })
                existing_names.add(name)
                new_count += 1
    
    # 添加民办院校
    for item in MORE_PRIVATE:
        name = item[0]
        if name not in existing_names:
            max_id += 1
            universities.append({
                'id': max_id,
                'name': item[0],
                'code': item[1],
                'province': item[2],
                'city': get_city(item[2], item[0]),
                'level': '普通本科',
                'type': item[3],
                'nature': item[4],
                'ranking': max_id + 700
            })
            existing_names.add(name)
            new_count += 1
    
    print(f"新增 {new_count} 所院校")
    print(f"院校总数: {len(universities)}")
    
    # 保存数据
    with open('./data/universities.json', 'w', encoding='utf-8') as f:
        json.dump(universities, f, ensure_ascii=False, indent=2)
    print("院校数据已保存")

if __name__ == '__main__':
    main()